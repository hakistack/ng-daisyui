#!/usr/bin/env node
/**
 * Extract every Tailwind-class-like token from the lib's compiled FESM
 * and emit a safelist file the daisyUI v4 plugin imports at runtime.
 *
 * Why:
 *   Per Tailwind v3 docs (https://v3.tailwindcss.com/docs/presets), a
 *   consumer's top-level `content: [array]` REPLACES (not concatenates)
 *   the preset's content array. There is "no built-in mechanism for
 *   libraries to automatically contribute content paths" (Tailwind v3
 *   Content Configuration docs). The only library-side workaround is a
 *   plugin-contributed `safelist`, which IS reliably merged.
 *
 *   Hand-curated safelists drift out of sync with the lib's actual
 *   class usage. This script keeps them in sync automatically by
 *   running a Tailwind-equivalent extractor over the FESM at build
 *   time and writing the result into a CommonJS module the plugin
 *   spreads into its safelist.
 *
 * Output: dist/hakistack/ng-daisyui/themes/fesm-classes.cjs
 *   exports a flat string[] of every candidate token. Tailwind v3
 *   silently drops entries that don't match any utility, so over-
 *   matching is harmless (just config-time bytes).
 *
 * Run as part of `npm run build` — see the build script in
 * ../package.json. Re-runs whenever the FESM is rebuilt.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const FESM_PATH = resolve(ROOT, 'dist/hakistack/ng-daisyui/fesm2022/hakistack-ng-daisyui.mjs');
const OUTPUT_PATH = resolve(ROOT, 'dist/hakistack/ng-daisyui/themes/fesm-classes.cjs');

// Curated allowlist of bare Tailwind classes (single-segment, no dash/colon/slash).
// Used downstream to keep tokens like "flex"/"grid" while dropping JS identifier
// words like "constructor"/"addEventListener" that the broad regex also matches.
const BARE_CLASSES = new Set([
  'flex', 'grid', 'block', 'inline', 'hidden', 'table', 'contents',
  'static', 'fixed', 'absolute', 'relative', 'sticky', 'isolate',
  'visible', 'invisible', 'collapse', 'truncate', 'italic', 'underline',
  'overline', 'antialiased', 'subpixel', 'uppercase', 'lowercase',
  'capitalize', 'normal', 'tabular', 'oldstyle', 'lining', 'overflow',
  'pointer', 'select', 'resize', 'shrink', 'grow', 'group', 'peer',
  'container', 'sr', 'transition', 'transform', 'rounded', 'border',
  'shadow', 'opacity', 'cursor', 'aspect',
]);

// Mirrors Tailwind v3's default broad extractor — captures any "class-like"
// token. Letters, digits, dashes, underscores, slashes (for opacity
// modifiers), colons (for variants), brackets (for arbitrary values),
// dots (for negative-leading utilities). Tokens are filtered downstream
// for obvious non-classes.
const TOKEN_RE = /[a-zA-Z\-][a-zA-Z0-9_\-:/[\]@%&'?*+=!.]*/g;

const fesm = readFileSync(FESM_PATH, 'utf-8');
const matches = fesm.matchAll(TOKEN_RE);
const seen = new Set();

for (const m of matches) {
  const tok = m[0];
  // Quick filters to drop obvious non-classes — tokens that are clearly
  // identifiers, function calls, file paths, or natural-language words.
  if (tok.length < 2) continue;
  if (tok.length > 80) continue;
  if (tok.includes('.')) {
    // Drop dots — most Tailwind classes don't contain them.
    continue;
  }
  // Drop pure-alpha identifiers ≥4 chars that don't contain a class-like
  // separator (-, :, /, [). Catches words like "constructor", "addEventListener",
  // while keeping single-segment classes like "flex" or "grid".
  if (/^[a-zA-Z]{4,}$/.test(tok)) {
    if (!BARE_CLASSES.has(tok)) continue;
  }
  // Drop tokens that look like JS identifiers in camelCase
  if (/^[a-z][a-zA-Z]{3,}[A-Z]/.test(tok)) continue;
  seen.add(tok);
}

const tokens = [...seen].sort();

const output = `// AUTO-GENERATED by scripts/extract-fesm-classes.mjs — DO NOT EDIT BY HAND.
// Re-run \`npm run build\` to refresh.
//
// This list is spread into the daisyUI v4 plugin's safelist so every
// Tailwind class used inside the lib's FESM bundle gets compiled into
// the consumer's CSS, even when the consumer's top-level \`content\`
// array prevents the preset from contributing scan paths (see
// docs/CHANGELOG entry for 0.1.89).

module.exports = ${JSON.stringify(tokens, null, 2)};
`;

writeFileSync(OUTPUT_PATH, output);
console.log(`✓ Extracted ${tokens.length} class candidates → ${OUTPUT_PATH}`);
