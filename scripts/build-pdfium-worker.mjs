#!/usr/bin/env node
/**
 * Bundle the PDFium render worker into a single, self-contained module and
 * **inline it into the library** so consumers configure NOTHING.
 *
 * esbuild bundles `pdfium.worker.ts` together with:
 *   - the `pdf_engine` wasm-bindgen glue (`src/lib/wasm/pdf/`, from build:wasm),
 *   - the emscripten PDFium ES6 module (`src/lib/wasm/pdfium/pdfium.js`),
 *   - BOTH `.wasm` binaries (via esbuild's `binary` loader → embedded as bytes).
 *
 * The bundled worker source is then written to `pdfium_worker_inline.ts` as a
 * string export. At runtime the loader makes a Blob + `new Worker(blobUrl,
 * { type: 'module' })` — no fetch, no served asset, no consumer setup.
 *
 * Must run **before** `ng build` so ng-packagr bundles the generated source
 * into the FESM (it's in the `build` chain ahead of the Angular build).
 *
 * Cost: a large **lazy** chunk (~9 MB, only loaded when a PDF first opens) and a
 * `worker-src blob:` CSP allowance. Trade for true zero-config.
 *
 * Requirements (skips gracefully until met):
 *   - `src/lib/wasm/pdf/pdf_engine.js` + `pdf_engine_bg.wasm`  → `npm run engine:build`
 *   - `engine/pdfium/pdfium.js` + `pdfium.wasm`               → vendor a
 *     `-sMODULARIZE -sEXPORT_ES6 -sENVIRONMENT=web,worker` PDFium build
 *     (the `-sENVIRONMENT=web,worker` flag strips Node branches so esbuild can
 *     bundle it cleanly). NOTE: vendored under engine/pdfium/, NOT src/lib/wasm/
 *     — `engine:build` rm -rf's the whole wasm/ tree. See PDFIUM_ENGINE.md §9b.
 */
import { build } from 'esbuild';
import { existsSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const lib = join(root, 'projects/hakistack/ng-daisyui/src/lib');
const engineDir = join(lib, 'components/pdf-viewer/engine');
const worker = join(engineDir, 'pdfium.worker.ts');
const inputs = [
  worker,
  join(lib, 'wasm/pdf/pdf_engine.js'),
  join(lib, 'wasm/pdf/pdf_engine_bg.wasm'),
  join(engineDir, 'pdfium/pdfium.js'),
  join(engineDir, 'pdfium/pdfium.wasm'),
];

const missing = inputs.filter((p) => !existsSync(p));
if (missing.length) {
  console.log('⏭  build-pdfium-worker: skipped — missing inputs:');
  for (const m of missing) console.log(`     ${m.replace(root + '/', '')}`);
  console.log('     (run `npm run engine:build` + vendor a MODULARIZE/ES6 pdfium build — PDFIUM_ENGINE.md §9b)');
  process.exit(0);
}

const result = await build({
  entryPoints: [worker],
  bundle: true,
  write: false,
  format: 'esm',
  platform: 'browser',
  target: 'es2022',
  minify: true,
  legalComments: 'none',
  loader: { '.wasm': 'binary' },
  // pdfium.esm.js is built with auto-environment detection: a dead
  // `if (ENVIRONMENT_IS_NODE) { await import("module"); require("fs"); … }`
  // branch (never taken in a worker). Mark Node builtins external so esbuild
  // leaves those guarded imports alone instead of trying to bundle them.
  external: ['module', 'fs', 'path', 'url', 'crypto', 'node:module', 'node:fs', 'node:path', 'node:url', 'node:crypto'],
});

const workerSrc = result.outputFiles[0].text;
const outFile = join(engineDir, 'pdfium/pdfium_worker_inline.ts');
// JSON.stringify (not base64) keeps it single-encoded; the embedded .wasm is
// already base64 inside `workerSrc` from esbuild's binary loader.
writeFileSync(
  outFile,
  `// Auto-generated by scripts/build-pdfium-worker.mjs. Do NOT edit or commit.\n` +
    `// Self-contained PDFium render worker (pdfium + pdf_engine wasm embedded);\n` +
    `// large + regenerated each build. The committed placeholder is the empty version.\n` +
    `export const PDFIUM_WORKER_SRC = ${JSON.stringify(workerSrc)};\n`,
);

// Flip the build flag so isPdfiumEngineAvailable() reports true.
writeFileSync(
  join(engineDir, 'pdfium/pdfium-built.ts'),
  `// Auto-generated by scripts/build-pdfium-worker.mjs. Do NOT edit or commit.\n` +
    `export const PDFIUM_WORKER_BUILT = true;\n`,
);

const mb = (n) => `${(n / 1024 / 1024).toFixed(1)} MB`;
console.log(`✓ Bundled PDFium worker → ${outFile.replace(root + '/', '')} (${mb(workerSrc.length)})`);
