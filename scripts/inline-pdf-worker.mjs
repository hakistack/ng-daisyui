#!/usr/bin/env node
/**
 * Pre-build script: inline pdf.worker.min.mjs as a string constant in the lib source.
 *
 * The PDF viewer component lazy-loads `pdfjs-dist` and needs a separate worker
 * file to parse PDFs off the main thread. Shipping the worker as a static asset
 * would force consumers to add it to their `angular.json` `assets` config — the
 * exact "consumer config" friction we don't want.
 *
 * Instead, this script reads `pdf.worker.min.mjs` and writes a generated TS
 * module (`pdf-viewer-worker.inlined.ts`) that exports the worker source as a
 * string constant. The component creates a Blob URL from that string at runtime
 * and points `pdfjs-dist`'s `GlobalWorkerOptions.workerSrc` at it. Zero consumer
 * config required.
 *
 * Run order: this MUST execute before `ng build`. The component imports the
 * generated file; if it doesn't exist, TypeScript fails fast with "cannot find
 * module './pdf-viewer-worker.inlined'." That's the desired behavior.
 *
 * Wired in two places:
 *   - workspace `package.json` `build` script (chains before `ng build`).
 *   - workspace `package.json` `postinstall` (so the file exists after a fresh
 *     `npm install`, keeping IDE type-checking happy on first clone).
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const __dirname = dirname(fileURLToPath(import.meta.url));

// Resolve pdfjs-dist via the package.json — works regardless of where npm
// hoisted it (workspace root, lib's local node_modules, etc.).
const pdfjsPkg = require.resolve('pdfjs-dist/package.json');
const pdfjsDir = dirname(pdfjsPkg);
const { version } = JSON.parse(readFileSync(pdfjsPkg, 'utf8'));

const workerPath = resolve(pdfjsDir, 'build/pdf.worker.min.mjs');
const workerSource = readFileSync(workerPath, 'utf8');

// Escape what a JS template literal interprets specially.
// Order matters: backslashes first, then backticks and ${ — otherwise we'd
// double-escape escapes we just added.
const escaped = workerSource
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$\{/g, '\\${');

const out = `// @ts-nocheck
// GENERATED FILE — do not edit. Regenerated by \`scripts/inline-pdf-worker.mjs\`
// from pdfjs-dist@${version}/build/pdf.worker.min.mjs on each build / postinstall.
/* eslint-disable */
export const PDF_WORKER_SOURCE = \`${escaped}\`;
export const PDF_WORKER_VERSION = ${JSON.stringify(version)};
`;

const outPath = resolve(
  __dirname,
  '../projects/hakistack/ng-daisyui/src/lib/components/pdf-viewer/pdf-viewer-worker.inlined.ts',
);
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out, 'utf8');

console.log(
  `[inline-pdf-worker] Wrote ${(out.length / 1024).toFixed(0)} KB → ${outPath.replace(resolve(__dirname, '..') + '/', '')}`,
);
