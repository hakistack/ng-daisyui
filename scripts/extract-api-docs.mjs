#!/usr/bin/env node
/**
 * Extract `@Component` API metadata from the lib's source files into a
 * typed JSON-like TS module that the demo app consumes via
 * `<app-api-docs-for>`.
 *
 * What we pull out per component:
 *   - class name (e.g. EditorComponent)
 *   - selector (`@Component({ selector: 'hk-editor' })`)
 *   - class-level JSDoc summary (first /** ... *​/ above the class)
 *   - inputs (every `readonly foo = input(...)` property, including the
 *     `.required` variant) with name, type, default literal, JSDoc summary
 *   - outputs (every `readonly bar = output<...>()` property) with name,
 *     payload type, JSDoc summary
 *
 * Why a Node script (not Compodoc): Compodoc's JSON shape becomes a
 * coupling point and we'd map its schema to ours anyway. ts-morph keeps
 * the output format under our control with much less AST boilerplate
 * than the raw TS Compiler API.
 *
 * Run via `npm run extract-api-docs`. Re-run when component sources change.
 */
import { Project, SyntaxKind } from 'ts-morph';
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const COMPONENTS_GLOB = 'projects/hakistack/ng-daisyui/src/lib/components/**/*.component.ts';
const OUTPUT_PATH = 'projects/shared-demos/api-docs.generated.ts';

const project = new Project({
  tsConfigFilePath: resolve(ROOT, 'projects/hakistack/ng-daisyui/tsconfig.lib.json'),
  skipAddingFilesFromTsConfig: true,
});
project.addSourceFilesAtPaths(resolve(ROOT, COMPONENTS_GLOB));

/** @type {Record<string, import('./extract-api-docs.types.js').ComponentApi>} */
const apiByComponent = {};

for (const sourceFile of project.getSourceFiles()) {
  for (const cls of sourceFile.getClasses()) {
    const componentDecorator = cls.getDecorator('Component');
    if (!componentDecorator) continue;

    const className = cls.getName();
    if (!className) continue;
    // Skip non-exported (e.g. internal helper components).
    if (!cls.isExported()) continue;

    const selector = readDecoratorStringProp(componentDecorator, 'selector') ?? '';
    const description = readClassJsDoc(cls);

    const inputs = [];
    const outputs = [];

    for (const prop of cls.getProperties()) {
      const init = prop.getInitializer();
      if (!init) continue;
      const initText = init.getText();

      const inputMatch = matchSignalCall(initText, 'input');
      const outputMatch = matchSignalCall(initText, 'output');

      if (inputMatch) {
        inputs.push({
          name: prop.getName(),
          type: resolveInputType(prop, init, inputMatch),
          default: resolveInputDefault(initText, inputMatch),
          description: readPropertyJsDoc(prop),
        });
      } else if (outputMatch) {
        outputs.push({
          name: prop.getName(),
          type: resolveOutputType(initText, outputMatch),
          description: readPropertyJsDoc(prop),
        });
      }
    }

    if (inputs.length === 0 && outputs.length === 0) continue;

    apiByComponent[className] = {
      className,
      selector,
      description,
      inputs,
      outputs,
    };
  }
}

const sorted = Object.fromEntries(Object.entries(apiByComponent).sort(([a], [b]) => a.localeCompare(b)));

const outputSource = `// AUTO-GENERATED by scripts/extract-api-docs.mjs — DO NOT EDIT BY HAND.
// Re-run \`npm run extract-api-docs\` after changing component inputs / outputs.

export interface ApiInputEntry {
  readonly name: string;
  readonly type: string;
  readonly default?: string;
  readonly description: string;
}

export interface ApiOutputEntry {
  readonly name: string;
  readonly type: string;
  readonly description: string;
}

export interface ComponentApi {
  readonly className: string;
  readonly selector: string;
  readonly description: string;
  readonly inputs: readonly ApiInputEntry[];
  readonly outputs: readonly ApiOutputEntry[];
}

// Pattern: keep literal-typed keys for the ApiComponentName union, but
// widen the entry type so consumers can read optional fields like
// inputs[i].default uniformly without TS narrowing them away on items
// where the key happens to be absent in the literal.
const _API_DOCS = ${JSON.stringify(sorted, null, 2)} as const;

export const API_DOCS: Record<keyof typeof _API_DOCS, ComponentApi> = _API_DOCS;

export type ApiComponentName = keyof typeof _API_DOCS;
`;

const outPath = resolve(ROOT, OUTPUT_PATH);
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, outputSource);

const componentCount = Object.keys(sorted).length;
const totalInputs = Object.values(sorted).reduce((n, c) => n + c.inputs.length, 0);
const totalOutputs = Object.values(sorted).reduce((n, c) => n + c.outputs.length, 0);
console.log(`✓ Extracted ${componentCount} components — ${totalInputs} inputs, ${totalOutputs} outputs → ${OUTPUT_PATH}`);

// ── helpers ────────────────────────────────────────────────────────────

function readDecoratorStringProp(decorator, name) {
  const args = decorator.getArguments();
  if (args.length === 0) return null;
  const obj = args[0];
  if (!obj.isKind(SyntaxKind.ObjectLiteralExpression)) return null;
  const prop = obj.getProperty(name);
  if (!prop || !prop.isKind(SyntaxKind.PropertyAssignment)) return null;
  const init = prop.getInitializer();
  if (!init) return null;
  if (init.isKind(SyntaxKind.StringLiteral)) return init.getLiteralText();
  if (init.isKind(SyntaxKind.NoSubstitutionTemplateLiteral)) return init.getLiteralText();
  return null;
}

function readClassJsDoc(cls) {
  const ranges = cls.getJsDocs();
  if (ranges.length === 0) return '';
  return summarizeJsDoc(ranges[ranges.length - 1].getDescription());
}

function readPropertyJsDoc(prop) {
  const ranges = prop.getJsDocs();
  if (ranges.length === 0) return '';
  return summarizeJsDoc(ranges[ranges.length - 1].getDescription());
}

/** Collapse a multi-line JSDoc body into a single-line description. */
function summarizeJsDoc(raw) {
  return raw
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean)
    .join(' ')
    .trim();
}

/**
 * Detect `input(...)`, `input.required(...)`, `output(...)` style call
 * expressions. Returns the captured first argument source text, or null
 * if the initializer doesn't match.
 */
function matchSignalCall(initText, kind) {
  // input(...) or input.required(...) — match either form
  const re =
    kind === 'input'
      ? /^input(?:\.required)?\s*(?:<([^>]+)>)?\s*\((.*)\)\s*$/s
      : /^output\s*(?:<([^>]+)>)?\s*\((.*)\)\s*$/s;
  const m = re.exec(initText);
  if (!m) return null;
  return { generic: m[1] ?? null, args: m[2] ?? '' };
}

function resolveInputType(prop, init, match) {
  // 1) Explicit generic on the input call: `input<MyType>('default')`
  if (match.generic) return match.generic.trim();

  // 2) Explicit type annotation on the property: `readonly x: string = input('')`
  const typeNode = prop.getTypeNode();
  if (typeNode) return typeNode.getText();

  // 3) Inferred type from the resolved property type — strip the
  //    `InputSignal<...>` / `InputSignalWithTransform<...>` wrappers so
  //    consumers see the user-facing payload type.
  const resolved = prop.getType().getText(prop);
  const unwrapped = unwrapSignalType(resolved);
  if (unwrapped) return unwrapped;

  // 4) Last resort — show the raw resolved type.
  return resolved;
}

function resolveOutputType(initText, match) {
  if (match.generic) return match.generic.trim();
  return 'void';
}

/**
 * Pluck the default-value argument from `input('default', { ... })`.
 * Returns the first-argument source text, or undefined for `input.required(...)`
 * (which has no default). The CST handles nesting via balanced-bracket walk.
 */
function resolveInputDefault(initText, match) {
  if (/^input\.required/.test(initText)) return undefined;
  const args = match.args.trim();
  if (!args) return undefined;
  const first = sliceFirstArg(args);
  return first ? first.trim() : undefined;
}

/** Walk the comma-separated arg list and return the first balanced segment. */
function sliceFirstArg(text) {
  let depth = 0;
  let inStr = null;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inStr) {
      if (c === inStr && text[i - 1] !== '\\') inStr = null;
      continue;
    }
    if (c === '"' || c === "'" || c === '`') {
      inStr = c;
      continue;
    }
    if (c === '(' || c === '[' || c === '{') depth++;
    else if (c === ')' || c === ']' || c === '}') depth--;
    else if (c === ',' && depth === 0) return text.slice(0, i);
  }
  return text;
}

function unwrapSignalType(typeText) {
  // Match `InputSignal<T>`, `InputSignalWithTransform<T, _>`, etc.
  const m = /^Input(?:Signal|SignalWithTransform)<([^,>]+)(?:,[^>]*)?>$/.exec(typeText.trim());
  return m ? m[1].trim() : null;
}
